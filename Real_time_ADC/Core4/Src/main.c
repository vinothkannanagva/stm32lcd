// C function to find maximum in arr[] of size n
uint16_t largestnumber(uint16_t arr[], uint16_t n)
{
    uint16_t i;

    // Initialize maximum element
    uint16_t max = arr[0];

    // Traverse array elements from second and
    // compare every element with current max
    for (i = 1; i < n; i++)
        if (arr[i] > max)
        {
            max = arr[i];
        }


    return max;
}

